public class PersonaCliente {
    int idPersonaCliente;
    int idCliente;
    int idPersona;
    int idUsuairo;

    public PersonaCliente() {
    }

    public int getIdPersonaCliente() {
        return idPersonaCliente;
    }

    public void setIdPersonaCliente(int idPersonaCliente) {
        this.idPersonaCliente = idPersonaCliente;
    }

    public int getIdCliente() {
        return idCliente;
    }

    public void setIdCliente(int idCliente) {
        this.idCliente = idCliente;
    }

    public int getIdPersona() {
        return idPersona;
    }

    public void setIdPersona(int idPersona) {
        this.idPersona = idPersona;
    }

    public int getIdUsuairo() {
        return idUsuairo;
    }

    public void setIdUsuairo(int idUsuairo) {
        this.idUsuairo = idUsuairo;
    }
    
    
}
